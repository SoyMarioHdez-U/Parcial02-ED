    private void btnImportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss"); 
        
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        File selectedFile= null;
        if (result == JFileChooser.APPROVE_OPTION)
        {
            selectedFile = fileChooser.getSelectedFile();
            this.jTextArea1.setText("Archivo seleccionado: ");
        }

        try
        {
            Scanner scan = new Scanner(new File(selectedFile.getAbsolutePath()));
//            scan.nextLine();
            int usuario = 0;
            while(scan.hasNextLine())
            {
                
                String sLinea = scan.nextLine();
                String[] aDatos = sLinea.split(",");
                
                if (aDatos[0].equals("+")){
                    Usuario uTemp = new Usuario(aDatos[1],aDatos[2], aDatos[3], Integer.parseInt(aDatos[4]));
                    this.lUs.add(uTemp);
                    usuario++;
                }
                else
                {
                    this.lUs.get(usuario-1).listaLibros.add(
                        new Libros( aDatos[1], aDatos[2], formato.parse(aDatos[3])));
                }
                //
                
            }
            this.jTextArea1.setText("¡Archivo CSV importado con éxito!");
            this.mostrarUsuarios();
            this.totalizarLibros();
        }
        catch(Exception e)
        {
            this.jTextArea1.setText(e.toString());
        }
    }//GEN-LAST:event_btnImportarActionPerformed


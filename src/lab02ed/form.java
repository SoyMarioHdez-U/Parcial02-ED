    private void jComboBoxUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxUsuariosActionPerformed
        // TODO add your handling code here:
        String opcion = (String)jComboBoxUsuarios.getSelectedItem();
        
        if(opcion.equals("Nombre (A~Z)"))
        {
            OrdenarPorNombreAsc oNas = new OrdenarPorNombreAsc();
            this.lUs.sort(oNas);
            this.mostrarUsuarios();
        }
        if(opcion.equals("Nombre (Z~A)"))
        {
            OrdenarPorNombreDesc oNdes = new OrdenarPorNombreDesc();
            this.lUs.sort(oNdes);
            this.mostrarUsuarios();
        }
        if(opcion.equals("Cantidad de libros (Menor~Mayor)"))
        {
            OrdenarPorCantidadMen oLmenamay = new OrdenarPorCantidadMen();
            this.lUs.sort(oLmenamay);
            this.mostrarUsuarios();
        }
        if(opcion.equals("Cantidad de libros (Mayor~Menor)"))
        {
            OrdenarPorCantidadMay oLmayamen = new OrdenarPorCantidadMay();
            this.lUs.sort(oLmayamen);
            this.mostrarUsuarios();
        }
    }//GEN-LAST:event_jComboBoxUsuariosActionPerformed


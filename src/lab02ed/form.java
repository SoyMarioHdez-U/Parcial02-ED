    //INTENTO DE OBTENER EL ÍNDICE DESDE EL ARRAY
    public int obtenerIndice(){
        String nombre = String.valueOf(this.jTableUsers.getValueAt(this.jTableUsers.getSelectedRow(),0));
        Boolean recorrer=true; 
        int cont=-1;
        
        while(recorrer)
        {
            cont++;
            if (this.lUs.get(cont).nombre.equals(nombre)) recorrer=false;
            
        }

        return cont;
    }
    
    
    
    
    
     private void mostrarUsuarios() {
        modeloUsers.setRowCount(0);
            for(int i=0; i<this.lUs.size(); i++)
            {
                String[] registroU = {this.lUs.get(i).nombre, this.lUs.get(i).carnet, this.lUs.get(i).rol, Integer.toString(this.lUs.get(i).cantLibros) };
                modeloUsers.addRow(registroU);
            }
    } 
        
     
    private void cargarLibros(){
        modeloLibros.setRowCount(0);
        int indiceUsuario = this.obtenerIndice();
        int cantLibros = this.lUs.get(indiceUsuario).listaLibros.size();
        
        
        for(int i=0; i<cantLibros; i++)
        {
            String[] lLibros = {this.lUs.get(indiceUsuario).listaLibros.get(i).nombrelibro, this.lUs.get(indiceUsuario).listaLibros.get(i).idioma,  formatoFecha.format(this.lUs.get(indiceUsuario).listaLibros.get(i).fecha)};
            modeloLibros.addRow(lLibros);
        }
        
        if(cantLibros==10){
            this.btnAddLibros.setEnabled(false);
        }
        this.cargarBarra();
    }
    
    private void libroIngresado(){
        int indiceUsuario = this.obtenerIndice();
        int subTotal = this.lUs.get(indiceUsuario).cantLibros;
        this.lUs.get(indiceUsuario).cantLibros = subTotal+1;
      
        this.txtLibro.setText("");
        this.checkBoxEspanyol.setSelected(false);
        this.checkBoxIngles.setSelected(false);
        this.cargarLibros();
        this.totalizarLibros();
        this.mostrarUsuarios();
        
        JOptionPane.showMessageDialog(null, "El registro se guardó correctamente en la lista de: "+ this.lUs.get(indiceUsuario).nombre, "Guardado", JOptionPane.INFORMATION_MESSAGE);
        this.lblCantidadLibros.setText("(Libros añadidos: 0/10)");
        this.barLibrosCant.setValue(0);
         this.txtBusqRes.setText(" ");
         //this.txtUsuario.setText(" ");
         //modeloLibros.setRowCount(0);
        
    }
    
    private void totalizarLibros(){
        int maxUsers = this.lUs.size();
        int total = 0;
        
        for(int i=0; i<maxUsers; i++){
            
                total+= this.lUs.get(i).cantLibros;
            
        }
        this.lblTotal.setText(String.valueOf(total));

    }
    
    private void cargarBarra(){
        int indiceUsuario = this.obtenerIndice();
        int cantidadLibros = this.lUs.get(indiceUsuario).listaLibros.size();
        this.barLibrosCant.setValue(cantidadLibros);
        this.lblCantidadLibros.setText("(Libros añadidos: "+ cantidadLibros +"/10)");      
    }
    

            
